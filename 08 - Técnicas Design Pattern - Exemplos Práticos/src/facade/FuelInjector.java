package facade;

public class FuelInjector {
	public void on() {
		System.out.println("Ativando sistema de injeção de combustível!");
	}
	public void inject() {
		System.out.println("Injetando combustível no motor!");
	}
	public void off() {
		System.out.println("Desativando sistema de injeção de combustível!");
	}
}
