package lacos;

public class Main {
	public static void main(String[] args) {
		
		// Contador
		int contador = 0;
		
		// Laço - while
		while (contador < 11) {
			System.out.println(contador++);
		}
		
		// Laço - Do While
		do {
			System.out.println(contador++);
		} while (contador < 11);
		
		// Laço - for
		for (contador = 0; contador < 11; contador++) {
			System.out.println(contador);
		}
		
		/*
		 	+=2
		 	-=1
		 	*=
		 	/=
		 	++
		 	--
		 */
		
	}
}
